# Drill 1: Create a variable x = 0
x = 0

# Drill 2: Write a while loop: while x < 5, print x, then increase x by 1
while x < 5:
    print(x)
    x = x +1
# Drill 3: Write a while loop: while x > 0, print x, then decrease x by 1
while x > 0:
    print(x)
    x = x - 1
# Drill 4: Create variable running = True. Write `while running:`, print "Hi" and set running = False
running = True
while running:
    print('Hi')
    running = False
