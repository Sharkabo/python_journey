# Drill 1: Create a list called numbers = [1, 2, 3, 4, 5]
numbers = [1, 2, 3, 4, 5]

# Drill 2: Write a for loop that prints each number in the list
for number in numbers:
    print(number)

# Drill 3: Write a for loop that prints each number multiplied by 10
for number in numbers:
    print(number*10)

# Drill 4: Write a loop using range(5) that prints "Hello" 5 times
for i in range(5):
    print('Hello')

# Drill 5: Write a loop using range(10) that prints the number (0 to 9)
for i in range(10):
    print(i)