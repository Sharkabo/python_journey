# Drill 1: Ask the user "What is your food?" and store it in variable 'food'
food = input('What is your food?')

# Drill 2: Print "I like " + food
print(f'I like {food}')

# Drill 3: Ask "Enter a number:" and store in 'num_text'
num_text = input('Enter a number')

# Drill 4: Convert 'num_text' to an integer and store in 'num'
num = int(num_text)

# Drill 5: Print 'num' plus 10
print(num+10)